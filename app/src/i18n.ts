export function loc<T extends Record<string, unknown> | null | undefined>(
  lang: Lang,
  obj: T,
  key: string,
): string {
  if (!obj) return '';
  if (lang === 'he') {
    const v = (obj as Record<string, unknown>)[key + 'He'];
    if (v === undefined || v === null || v === '') {
      return ((obj as Record<string, unknown>)[key] as string) ?? '';
    }
    return v as string;
  }
  return ((obj as Record<string, unknown>)[key] as string) ?? '';
}
